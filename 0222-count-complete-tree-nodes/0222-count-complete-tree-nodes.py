class Solution:
    def countNodes(self, root):
        if not root:
            return 0

        def left_height(node):
            h = 0
            while node:
                h += 1
                node = node.left
            return h

        def right_height(node):
            h = 0
            while node:
                h += 1
                node = node.right
            return h

        lh = left_height(root)
        rh = right_height(root)

        if lh == rh:
            return (1 << lh) - 1  # perfect binary tree
        else:
            return 1 + self.countNodes(root.left) + self.countNodes(root.right)
    